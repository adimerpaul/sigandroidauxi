    /**
     * Manipulates the map once available.
     * This callback is triggered when the map is ready to be used.
     * This is where we can add markers or lines, add listeners or move the camera. In this case,
     * we just add a marker near Sydney, Australia.
     * If Google Play services is not installed on the device, the user will be prompted to install
     * it inside the SupportMapFragment. This method will only be triggered once the user has
     * installed Google Play services and returned to the app.
     */
    @Override
    public void onMapReady(GoogleMap googleMap) {
        mMap = googleMap;

        // Add a marker in Sydney and move the camera
        LatLng sydney = new LatLng( -17.990783, -67.132807);
//        mMap.addMarker( new MarkerOptions().position( sydney ).title( "Marker in Sydney" ) );
        mMap.moveCamera( CameraUpdateFactory.newLatLngZoom( sydney,14 ) );



        db.collection("pedidos")
                .whereEqualTo("estado", "pendiente")
                .addSnapshotListener(new EventListener<QuerySnapshot>() {
                    @Override
                    public void onEvent(@Nullable QuerySnapshot value,
                                        @Nullable FirebaseFirestoreException e) {
                        if (e != null) {
                            Log.w(TAG, "Listen failed.", e);
                            return;
                        }
                        mMap.clear();
                        List<String> cities = new ArrayList<>();
                        for (QueryDocumentSnapshot doc : value) {
                            if (doc.get("lat") != null) {
//                                cities.add(doc.getDouble("lat"));
                                LatLng punto = new LatLng( doc.getDouble("lat"), doc.getDouble("lng"));
                                mMap.addMarker( new MarkerOptions().position( punto ).title( "Marker in Sydney" ) );
                                Log.d(TAG, "Current cites in CA: " + doc.getDouble("lat"));
                            }
                        }

                    }
                });
    }
